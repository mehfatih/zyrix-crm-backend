// ============================================================================
// AI MODES (P11) — Architect / Builder / Report
// ----------------------------------------------------------------------------
// Three specialized Gemini endpoints:
//   • architect  — suggests initial CRM config from a business description
//   • builder    — generates one artifact (workflow, email, landing) from
//                  a one-line intent
//   • report     — picks a whitelisted aggregate query to answer a
//                  natural-language question, runs it, returns chart + text
//
// All calls go through gemini-2.0-flash via the existing GEMINI_API_KEY.
// SQL is NEVER generated by the model — the report mode only chooses
// which of a handful of pre-baked queries to execute, then narrates
// the result.
// ============================================================================

import { GoogleGenerativeAI } from "@google/generative-ai";
import { prisma } from "../config/database";
import { env } from "../config/env";
import { badRequest } from "../middleware/errorHandler";

const genAI = env.GEMINI_API_KEY
  ? new GoogleGenerativeAI(env.GEMINI_API_KEY)
  : null;

async function gemini(prompt: string): Promise<string> {
  if (!genAI) throw badRequest("AI is not configured on this tenant");
  const model = genAI.getGenerativeModel({ model: "gemini-2.0-flash" });
  const result = await model.generateContent(prompt);
  return result.response.text().trim();
}

function stripFences(s: string): string {
  return s.replace(/^```(?:json)?\s*/i, "").replace(/\s*```\s*$/i, "");
}

function safeJson<T>(s: string): T | null {
  try {
    return JSON.parse(stripFences(s)) as T;
  } catch {
    return null;
  }
}

// ──────────────────────────────────────────────────────────────────────
// ARCHITECT
// ──────────────────────────────────────────────────────────────────────

export interface ArchitectInput {
  businessDescription: string;
  locale?: "en" | "ar" | "tr";
}

export interface ArchitectOutput {
  pipelineStages: Array<{ name: string; probability?: number }>;
  customFields: Array<{
    entity: "customer" | "deal";
    name: string;
    type: "text" | "number" | "date" | "select";
    options?: string[];
  }>;
  emailTemplates: Array<{ name: string; subject: string; bodyText: string }>;
  rationale: string;
}

export async function architect(
  input: ArchitectInput
): Promise<ArchitectOutput> {
  const desc = input.businessDescription.trim();
  if (!desc) throw badRequest("businessDescription is required");
  const prompt = `You are configuring a new Zyrix CRM for a business.
Business: ${desc}

Return strict JSON with:
  pipelineStages: array of { name, probability (0-100) } between 4 and 7 stages
  customFields: array of { entity ("customer"|"deal"), name, type ("text"|"number"|"date"|"select"), options? (for select) }, max 6 items total
  emailTemplates: 2-3 templates with { name, subject, bodyText }, suited to the business
  rationale: 2-sentence explanation of the choices

Reply with ONLY the JSON, no code fences, no commentary.`;
  const raw = await gemini(prompt);
  const parsed = safeJson<ArchitectOutput>(raw);
  if (!parsed) {
    throw badRequest("AI returned non-JSON; try again with a longer business description");
  }
  return parsed;
}

